        // Dark Mode Toggle
        const darkModeToggle = document.getElementById('darkModeToggle');
        const darkModeText = document.getElementById('dark-mode-text');
        const darkModeIcon = darkModeToggle.querySelector('i');
        
        // Initialize dark mode
        function initializeDarkMode() {
            if (localStorage.getItem('darkMode') === 'enabled') {
                document.body.classList.add('dark-theme');
                darkModeIcon.classList.remove('fa-moon');
                darkModeIcon.classList.add('fa-sun');
            }
        }
        
        // Toggle dark mode
        darkModeToggle.addEventListener('click', () => {
            document.body.classList.toggle('dark-theme');
            const currentLang = getCurrentLanguage();
            
            if (document.body.classList.contains('dark-theme')) {
                localStorage.setItem('darkMode', 'enabled');
                darkModeIcon.classList.remove('fa-moon');
                darkModeIcon.classList.add('fa-sun');
                darkModeText.textContent = translations[currentLang].lightModeText;
            } else {
                localStorage.setItem('darkMode', 'disabled');
                darkModeIcon.classList.remove('fa-sun');
                darkModeIcon.classList.add('fa-moon');
                darkModeText.textContent = translations[currentLang].darkModeText;
            }
        });
        
        // Language Dropdown Functionality
        const languageSelector = document.querySelector('.language-selector');
        const languageDropdown = document.querySelector('.language-dropdown');
        const languageOptions = document.querySelectorAll('.language-option');
        const currentLangElement = document.getElementById('current-lang');
        
        // Get current language
        function getCurrentLanguage() {
            return localStorage.getItem('userLanguage') || 'en';
        }
        
        // Apply translations to the page
        function applyTranslations(lang) {
            const langData = translations[lang];
            if (!langData) return;
            
            // Update all text elements
            document.getElementById('home-label').textContent = langData.homeLabel;
            document.getElementById('home-link').textContent = langData.homeLink;
            document.getElementById('contact-label').textContent = langData.contactLabel;
            document.getElementById('contact-link').textContent = langData.contactLink;
            document.getElementById('support-title').textContent = langData.supportTitle;
            document.getElementById('support-description').textContent = langData.supportDescription;
            document.getElementById('patreon-title').textContent = langData.patreonTitle;
            document.getElementById('patreon-description').textContent = langData.patreonDescription;
            document.getElementById('patreon-button').textContent = langData.patreonButton;
            document.getElementById('coffee-title').textContent = langData.coffeeTitle;
            document.getElementById('coffee-description').textContent = langData.coffeeDescription;
            document.getElementById('coffee-button').textContent = langData.coffeeButton;
            document.getElementById('why-donate').textContent = langData.whyDonate;
            document.getElementById('reason1').textContent = langData.reason1;
            document.getElementById('reason2').textContent = langData.reason2;
            document.getElementById('reason3').textContent = langData.reason3;
            document.getElementById('reason4').textContent = langData.reason4;
            document.getElementById('reason5').textContent = langData.reason5;
            currentLangElement.textContent = langData.currentLang;
            
            // Update dark mode text
            if (document.body.classList.contains('dark-theme')) {
                darkModeText.textContent = langData.lightModeText;
            } else {
                darkModeText.textContent = langData.darkModeText;
            }
            
            // Set RTL for Arabic and Kurdish languages
            if (lang === 'ar' || lang === 'ku' || lang === 'ckb') {
                document.body.classList.add('rtl');
            } else {
                document.body.classList.remove('rtl');
            }
            
            // Save language preference
            localStorage.setItem('userLanguage', lang);
        }
        
        // Toggle dropdown
        languageSelector.addEventListener('click', (e) => {
            e.stopPropagation();
            languageDropdown.classList.toggle('active');
        });
        
        // Close dropdown when clicking outside
        document.addEventListener('click', (e) => {
            if (!languageDropdown.contains(e.target)) {
                languageDropdown.classList.remove('active');
            }
        });
        
        // Handle language selection
        languageOptions.forEach(option => {
            option.addEventListener('click', () => {
                languageOptions.forEach(opt => opt.classList.remove('active'));
                option.classList.add('active');
                const lang = option.getAttribute('data-lang');
                applyTranslations(lang);
            });
        });
        
        // Initialize page
        document.addEventListener('DOMContentLoaded', () => {
            initializeDarkMode();
            const savedLang = getCurrentLanguage();
            applyTranslations(savedLang);
            
            // Set active language option
            const activeOption = document.querySelector(`.language-option[data-lang="${savedLang}"]`);
            if (activeOption) {
                languageOptions.forEach(opt => opt.classList.remove('active'));
                activeOption.classList.add('active');
            }
            
            // Initialize dark mode text
            const currentLang = getCurrentLanguage();
            if (document.body.classList.contains('dark-theme')) {
                darkModeText.textContent = translations[currentLang].lightModeText;
            } else {
                darkModeText.textContent = translations[currentLang].darkModeText;
            }
        });
